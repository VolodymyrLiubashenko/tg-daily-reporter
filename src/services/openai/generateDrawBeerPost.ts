import { OpenAI } from "openai";
import { env } from "../../config/env";

const client = new OpenAI({
   apiKey: env.openaiApiKey,
});

type TInput = {
   activeUsers: {
      telegramUserId: string;
      messageCount: number;
      username: string | null;
      firstName: string | null;
      lastName: string | null;
   }[];
};

export async function generateDrawBeerPost(input: TInput) {
   const prompt = `
   Ти створюєш Telegram-пост українською мовою.
   
   ДАНІ КОРИСТУВАЧІВ:
   ${input.activeUsers.map((user, index) => `${index + 1}. username: ${user.username ?? "-"}, ім’я: ${user.firstName ?? ""} ${user.lastName ?? ""}`).join("\n")}
   
   ЗАВДАННЯ:
   - Напиши короткий, живий, природний текст українською
   - Уникай граматичних помилок і неприродних конструкцій
   - НЕ використовуй слова або фрази, що прив’язані до конкретного дня публікації: "сьогодні", "зараз", "цього разу" і т.д.
   - Обов’язково вкажи, що розіграш проходить у п’ятницю ввечері (орієнтовно о 20:00)
   - Обов’язково згадай, що це розіграш від "Барного оракула"
   - Вкажи, що активні учасники чату беруть участь у розіграші
   - Використовуй природні формулювання, наприклад:
     • "хто стане переможцем"
     • "кому усміхнеться удача"
     • "хто забере приз"
   - Додай 1 короткий вступ (1–2 рядки) про розіграш пива 🍺
   - Додай дружнє побажання всім учасникам виграти
   
   СПИСОК УЧАСНИКІВ:
   - Якщо користувачів більше 0 — покажи таблицю
   - Якщо користувачів 0 — НЕ показуй таблицю 
   
   ФОРМАТ ТАБЛИЦІ:
   № | Username
   1 | @username
   2 | @username
   
   ПРАВИЛА:
   - Не вигадуй користувачів
   - Якщо username відсутній — використовуй "Ім’я Прізвище"
   - Якщо немає імені або прізвища — використовуй те, що є
   - Не використовуй зайвий текст після таблиці
   - Дозволено 1–2 емодзі максимум
   - Не пиши пояснень, тільки готовий пост
   
   ВАЖЛИВО:
   Текст має звучати природно, як від живої людини.
   
   ПОВЕРНИ ТІЛЬКИ ТЕКСТ ПОСТА.
   `;

   const response = await client.responses.create({
      model: "gpt-5-mini",
      input: prompt,
   });

   return response.output_text;
}
