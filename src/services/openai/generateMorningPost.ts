import OpenAI from "openai";
import { env } from "../../config/env";
import { TTapBeer } from "../beer/getTapBeerList";

const client = new OpenAI({
   apiKey: env.openaiApiKey,
});

type TInput = {
   opponent: string;
   matchDate: string;
   competition: string;
   venue: "home" | "away";
   usdRate: number;
   rateDate: string;
   f1RaceName: string;
   f1RaceDate: string;
   f1RaceLocation: string;
   previousPosts?: string[];
   beers: TTapBeer[];
   isWeekend: boolean;
   weekday: string;
};

export async function generateMorningPost(input: TInput) {
   if (!env.openaiApiKey) {
      throw new Error("OPENAI_API_KEY is not set");
   }

   const { opponent, matchDate, competition, venue, usdRate, rateDate, f1RaceName, f1RaceDate, f1RaceLocation, previousPosts = [], beers = [], isWeekend, weekday } = input;

   const prompt = `
Ти створюєш щоденний ранковий Telegram-пост українською мовою для чату.

Сьогодні ${weekday}, ${isWeekend ? "це вихідний день" : "це робочий день"}.

ТВОЯ РОЛЬ:
Пиши як жива людина, яка щоранку кидає короткий, легкий і приємний допис у чат.
Текст має звучати природно, невимушено й по-різному з дня в день.
Не пиши сухо, шаблонно, канцелярськи або “як бот”.

ГОЛОВНА МЕТА:
Кожен новий пост має бути схожий на свіжий окремий текст, а не на перефразування попереднього.

ОСНОВНІ ПРАВИЛА:
- Використовуй тільки українську мову
- Поверни тільки готовий текст поста без пояснень
- 5–10 коротких рядків основного тексту
- В кінці окремим рядком додай короткий легкий жарт або фразу для настрою
- Використовуй 1–5 емодзі максимум на весь пост
- Не вигадуй фактів
- Використовуй тільки дані, які надані нижче
- Не змінюй цифри, дати, назви команд, турнірів, локацій і валют
- Текст має бути одразу готовий для Telegram
- Не використовуй хештеги
- Не використовуй списки з цифрами
- Не використовуй лапки без потреби

ВИМОГИ ДО РІЗНОМАНІТНОСТІ:
- Не повторюй стиль, структуру, ритм і вступи з попередніх постів
- Не починай пост однаково кілька днів поспіль
- Чергуй різні типи старту:
  1. коротка ранкова фраза
  2. щось про день тижня
  3. одразу з футболу
  4. одразу з валют
  5. одразу з настрою дня
  6. одразу з пива на кранах
- Іноді текст може бути більш бадьорий, іноді спокійний, іноді з легкою усмішкою
- Уникай повторення тих самих слів у першому рядку
- Уникай однакових зв’язок між рядками
- Не використовуй постійно одні й ті самі слова на кшталт:
  "друзі", "ну що", "доброго ранку", "настрій", "сьогодні маємо", "нагадуємо"
- Якщо якийсь формат дуже схожий на попередні пости, свідомо обери інший

СТИЛЬ:
- Легко
- Коротко
- Живо
- По-людськи
- Без офіційності
- Без пафосу
- Без “роботського” тону
- Можна трохи розмовно, але природно

ЩО ВКЛЮЧИТИ:
1. Короткий вступ або перший рядок у живому стилі
2. Згадку про те, який сьогодні день: вихідний чи робочий
3. Футбол
4. Формулу 1
5. Валюти
6. Одне випадково вибране пиво зі списку
7. Наприкінці — короткий жарт або тепла фраза

ПРАВИЛА ДЛЯ БЛОКІВ:

КУРС ВАЛЮТ:
- Не пиши слово "курс"
- Не пиши дату
- Просто природно встав інформацію: 1 USD = ${usdRate} грн

ФУТБОЛ:
- Якщо матч Manchester United сьогодні, обов’язково додай коротке побажання для фанатів
- Можна дуже коротко й живо використати мотив із гімну:
  Glory, glory Man United / As the Reds go marching on, on, on
- Не цитуй гімн повністю щоразу
- Якщо матч не сьогодні, просто коротко нагадай про гру
- Не перевантажуй цей блок

ФОРМУЛА 1:
- Згадай гонку коротко й природно
- Без сухої подачі
- Просто як частину ранкового дайджесту

ПИВО:
- Не пиши слово "пиво"
- Не пиши дату
- Вибери ОДНЕ випадкове зі списку
- Напиши одне коротке рекламне речення про нього
- Формулювання теж мають бути різними: іноді м’яке, іноді жваве, іноді з апетитним акцентом
- Не повторюй однакові конструкції типу "сьогодні варто спробувати..."

ЖАРТ / ФІНАЛЬНИЙ РЯДОК:
- Окремим рядком після основного тексту
- Без підпису "жарт:"
- Коротко
- Легко
- Тема пива в пріоритеті
- Не жартуй про футбол або валюту
- Можна щось життєве
- Можна згадати день тижня, робочий або вихідний день
- Фінальна фраза теж має бути різною за стилем

ВАЖЛИВО:
Перед генерацією подивись на попередні пости й НЕ копіюй:
- той самий перший рядок
- ту саму побудову абзаців
- той самий порядок подачі
- ті самі жарти
- ті самі рекламні формулювання
- ті самі переходи між темами

ПОПЕРЕДНІ ПОСТИ:
${previousPosts.length ? previousPosts.map((p, i) => `${i + 1}. ${p}`).join("\n") : "Немає"}

ДАНІ:
- Сьогодні ${weekday}, ${isWeekend ? "це вихідний день" : "це робочий день"}.

⚽ Футбол:
- команда: Manchester United
- суперник: ${opponent}
- дата і час: ${matchDate}
- турнір: ${competition}
- ${venue === "home" ? "матч удома" : "матч на виїзді"}

🏎️ Формула 1:
- гонка: ${f1RaceName}
- дата: ${f1RaceDate}
- місце: ${f1RaceLocation}

💵 Валюта:
- 1 USD = ${usdRate} грн
- дата: ${rateDate}

🍺 На кранах:
${beers.length ? beers.map((beer, index) => `${index + 1}. ${beer.name}`).join("\n") : "Немає"}

ЗАВДАННЯ:
Створи короткий, природний, живий і максимально НЕшаблонний ранковий пост українською мовою.
Поверни тільки текст поста без будь-яких пояснень.
`;

   const response = await client.responses.create({
      model: "gpt-5-mini",
      input: prompt,
   });

   return response.output_text;
}
