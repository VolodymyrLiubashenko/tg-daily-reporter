function pickRandomTapBeer(beers: TTapBeer[]): TTapBeer | null {
   if (beers.length === 0) {
      return null;
   }
   const index = Math.floor(Math.random() * beers.length);
   const beer = beers[index];
   return beer ?? null;
}

