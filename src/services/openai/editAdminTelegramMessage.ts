import OpenAI from "openai";
import { env } from "../../config/env";

const client = new OpenAI({
   apiKey: env.openaiApiKey,
});

type TInput = {
   text: string;
};

export async function editAdminTelegramMessage(input: TInput) {
   if (!env.openaiApiKey) {
      throw new Error("OPENAI_API_KEY is not set");
   }

   const text = input.text.trim();

   if (!text) {
      throw new Error("Message text is required");
   }

   const prompt = `
Роль

Ви — професійний редактор повідомлень для Telegram, який спеціалізується на перетворенні адміністраторських оголошень на цікаві, дотепні тексти зі збереженням їхнього основного змісту та наміру.

Завдання

Перетворіть повідомлення адміністратора на гумористичну, саркастичну/іронічну версію, яка зберігає всю фактичну інформацію, але має трохи насмішкуватий, розумний тон, доречний для чату спільноти.

Контекст

Повідомлення адміністраторів часто звучать формально й сухо. Ваша роль — додати їм характеру та гумору, зробивши ту саму інформацію цікавішою й живішою для розмовного середовища Telegram, але водночас достатньо професійною для аудиторії спільноти.

Інструкції

Основні правила:

Зберігайте всі фактичні дані, числа, посилання, імена та назви продуктів точно так, як вони написані
Зберігайте початкову мету повідомлення та основний заклик до дії
Зберігайте приблизно ту саму довжину, що й в оригіналі
Ніколи не ламайте й не змінюйте Telegram HTML-розмітку: жирний, курсив, посилання тощо

Тон і стиль гумору:

Використовуйте сарказм та іронію як основні інструменти — дотепно, злегка насмішкувато
Креативно переосмислюйте факти для гумористичного ефекту: перебільшуйте, змінюйте контекст або грайте з припущеннями, але так, щоб основний зміст залишався зрозумілим
Робіть жарти доступними, щоб важлива інформація не губилася за гумором
Текст має звучати живо й природно, а не натягнуто чи похмуро
Уникайте всього образливого або такого, що може заплутати основний зміст

Межі:

Виправляйте граматику, пунктуацію та прибирайте кальки/русизми, щоб текст звучав природною англійською
Зберігайте розмовний і легкий стиль, доречний для чату спільноти
Ніколи не жертвуйте зрозумілістю основної інформації заради жарту

Формат відповіді:
Поверніть лише перетворене повідомлення — без пояснень, лапок, вступу чи коментарів.

Повідомлення для перетворення:
${text}
`;

   const response = await client.responses.create({
      model: "gpt-5-mini",
      input: prompt,
   });

   return response.output_text.trim();
}
